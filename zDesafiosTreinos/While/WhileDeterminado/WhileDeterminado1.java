package zDesafiosTreinos.While.WhileDeterminado;

public class WhileDeterminado1 {
	
	public static void main(String[] args) {
		
		// fazer coisas simples com while
		// um While determinado é quando um se repete até que o objetivo se cumpra.
		
		int contador = 0;
		
		while(contador <= 10) {
			System.out.println("Num: " + contador);
			
			contador += 1;
		}
	}
}
