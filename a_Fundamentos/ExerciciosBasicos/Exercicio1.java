package a_Fundamentos.ExerciciosBasicos;

public class Exercicio1 {
	
	public static void main(String[] args) {
		
		// Exercicio de Conversão de Celsius para Fahrenheit
		
		double celsius1 = 22;
		double celsius = (celsius1 / 5) * 9;
		final int tf = 32;
		double resultado = tf + celsius;
		
		System.out.println("Celsius " + celsius1 + " em Fahrenheit é: " + resultado);
	}

}
