package b.Controle.For;

public class For2Decrescente {

	public static void main(String[] args) {
		
		/* While Decrescente deu certo
		int comeco = 10;	
		while(comeco >= 0) {
			System.out.println(comeco);
			
			comeco -= 2;
		}*/
		
		// for Decrescente deu certo
		for(int comeco = 10; comeco >= 0; comeco -= 2) {
			System.out.println(comeco);
		}
	}
}
